#include <bits/stdc++.h>
 
struct node
{
    int data;
	
    node *next;
};

const int MaxSize = 10;

//________________________________________________________________________________________________
// 
//________________________________________________________________________________________________
void Print (node *top)
{
    while (top != nullptr)
    {
        std::cout << top->data << std::endl;
		
        top = top->next; 
    }
}

//________________________________________________________________________________________________
// 
//________________________________________________________________________________________________
int Size (node *top)
{
	int count = 0;
	
	while (top != nullptr)
	{
		count++;
		
		top = top->next;
	}
	
	return count;
}

//________________________________________________________________________________________________
// 
//________________________________________________________________________________________________
bool isEmpty (node* top)
{
	return (Size(top) == 0 ? true : false);
}

//________________________________________________________________________________________________
// 
//________________________________________________________________________________________________
bool isFull (node* top)
{
    return (Size(top) == MaxSize ? true : false);
}

//________________________________________________________________________________________________
// 
//________________________________________________________________________________________________
void Push ( node **top, 
			int data )
{
    if (isFull(*top))
	{
        std::cout << "Stack Overflow!" << std::endl;
		
		exit(1);
    }
    else
	{
        node *newNode = new node;
		
        newNode->data = data;
        newNode->next = *top;
        *top = newNode;
    }
}

//________________________________________________________________________________________________
// 
//________________________________________________________________________________________________
void Pop (node **top)
{
    if (isEmpty(*top))
	{
        std::cout << "Stack Underflow!" << std::endl;
		
		exit(1);
    }
    else
	{
        node *temp = *top;
		
        *top = (*top)->next;
        
        delete temp; 
    }
}

//________________________________________________________________________________________________
// 
//________________________________________________________________________________________________
int Top (node *top)
{
	return top->data;
}

//________________________________________________________________________________________________
// 
//________________________________________________________________________________________________
int main()
{
	node* top = nullptr;
	
    Push(&top, 78);
    Push(&top, 7);
    Push(&top, 8);
    
    Print(top);
	
	std::cout << "Top element: " << Top(top) << std::endl;
	
	std::cout << std::endl << std::endl;
	
    Pop(&top);
	
	Print(top);
	
	std::cout << "Top element: " << Top(top) << std::endl;
	
    return 0;
}
